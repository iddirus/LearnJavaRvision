package basic1;

public class EscapeSequence {

	// Ecsape sequence
	
	
	public static void main(String[] args) {
		
	   System.out.println("this is QA engineering class");	
	   System.out.println("this is QA \tengineering class");
	   System.out.println("this is QA engineering \bclass");
	   // \b is not supported by eclipse
	   System.out.println("this is QA engineering \nclass");
	   System.out.println("this is QA \'engineering\' class");
	   System.out.println("this is QA \"engineering\" class");
	   System.out.println("this is \\QA engineering class");
	   System.out.println("this is QA engineering \rclass");
	   System.out.println("this is QA \rengineering class");
	   System.out.println("this is \fQA engineering class");
		// \f is not supported by eclipse
	   
		
	}
	
	
	
	
	
	
	
	
	
}
